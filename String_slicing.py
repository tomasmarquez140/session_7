s = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"

# index is also a very basic slide
print(s[1]) # B
print(s[1:3]) # from positon 1 to 3 excluded (excluded)
print(s[5:9])
print(s[:7]) # from start to postion 7
print(s[4:])
print(s[::]) # all of it
print(s[::2]) # all of it in steps of 2
print(s[:10:3]) # ADGJ # from first character to 10 in steps of 3
print(s[::-1])
